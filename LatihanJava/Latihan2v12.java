public class Latihan2v11{
     public static void main(String[] args) {

         int angka =1;
          System.out.printf("Unary '+', %d menjadi  %d\n", angka, + angka);
          System.out.printf("Unary '-', %d menjadi  %d\n", angka, - angka);

          int angka2 = 10;
          angka2++;
          System.out.println("Nilai dengan '++' menjadi " + angka2);


          int angka3 = 10;
          angka2--;
          System.out.println("Nilai dengan '--' menjadi " + angka3);


          int a = 5;
          System.out.printf("Nilai dengan '++' menjadi =%d\n ", ++a);


          int b = 5;
          System.out.printf("Nilai dengan '++' postfix menjadi =%d\n ", ++b);
          System.out.printf("Nilai dengan '++' menjadi =%d\n ", b);

          boolean nadya = true;
          System.out.println("Nilai boolean = " + nadya);
          System.out.println("Nilai boolean = " + !nadya);





     }


}