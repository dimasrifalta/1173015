public class Latihan2v10 {
    public static void main(String[]args){
        int var1 = 12;
        int var2 = 5;
        int hasil;

        //penjumlahan
        hasil = var1 + var2;
        System.out.println(var1 + " + " + var2 + " = " + hasil);

        //pengurangan
        hasil= var1 - var2;
        System.out.printf("%d - %d = %d \n",var1,var2,hasil);

        //perkalian
        hasil=var1*var2;
        System.out.printf("%d * %d = %d\n",var1,var2,hasil);

        //pembagian
        hasil= var1 / var2;
        System.out.printf("%d / %d = %d \n",var1,var2,hasil);

        float a =6;
        float b =5;
        float hasilfloat= a/b;
        System.out.println(a + " / " + b + " = " + hasilfloat);

        //modulus
        hasil=var1 % var2;
        System.out.printf("%d %% %d = %d\n",var1,var2,hasil);


    }
}